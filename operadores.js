//Operadores Aritméticos
console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(8/4);

//Operadores de comparação
console.log(2 == 2); //igual - retorna true se os valores são iguais independente do tipo
console.log(2 != 2); //diferente
console.log(2 === "2"); //igual estrito - retorna verdadeiro se o valor e tipo for igual
console.log(3 !== "3"); //diferente estrito

console.log(2>3); //maior que
console.log(2<3); //menor que
console.log(2>=2); //maior igual
console.log(2<=2); //menor igual